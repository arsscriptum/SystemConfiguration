Function Save-Profile{
    $Path = (Get-Item -Path $Profile).DirectoryName
    pushd "$Path\Profile"
    $GitExe = 'c:\Programs\Git\bin\git.exe'
    &"$GitExe" commit -a -m 'Latest Profile'
    &"$GitExe" push
    popd
}
